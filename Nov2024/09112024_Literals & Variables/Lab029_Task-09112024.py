# take input of 3 number & perform Sum, Sub, Mul, Div

Num1 = int(input("Enter the No 1"))
Num2 = int(input("Enter the No 2"))
Num3 = int(input("Enter the No 3"))

sum = Num1 + Num2 + Num3
sub = Num1 - Num2 - Num3
Mul = Num1 * Num2 * Num3
Div = Num1 / Num2 / Num3

print("Sum of 3 no's is :", sum)
print("Sub of 3 no's is :", sub)
print("Mul of 3 no's is :", Mul)
print("Div of 3 no's is :", Div)