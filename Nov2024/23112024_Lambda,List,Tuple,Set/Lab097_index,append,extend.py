# indexing

my_list = [1, 2, 3]
print(my_list[0])
print(my_list[1])
print(my_list[2])

# Append
my_list.append(4)
my_list.append(5)
print(my_list)

# Extend
my_list.extend([6, 7, 8, 9,11,10,12])
print(my_list)

# Insert
my_list.insert(3, "Ravi")
print(my_list)

# Remove
my_list.remove("Ravi")
print(my_list)

#sort
my_list.sort()
print(my_list)

# Pop -
print(my_list.pop())
print(my_list)

